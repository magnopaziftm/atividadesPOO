package classes;

public class Endereco {
	String logradouro;
	String cidade;
	String estado;
	int numero;
	String CEP;
	
	Endereco(String logradouro, String cidade, String estado, int numero, String CEP){
		this.logradouro = logradouro;
		this.cidade = cidade;
		this.estado = estado;
		this.numero = numero;
		this.CEP = CEP;
		
	}

}
